// Main search function that gets text embedding and searches Weaviate
export async function searchItemsByText(
  query: string,
  page: number,
  limit: number
): Promise<CatalogResponseSchemaType | ErrorSchemaType> {
  // Get text embedding from Python server
  const textEmbeddingResponse = await extractTextFeatures(query);
  if (
    textEmbeddingResponse.error ||
    textEmbeddingResponse.features.length === 0
  ) {
    return {
      error: true,
      details: textEmbeddingResponse.details,
    };
  }
  //imprimo completo el array
  logger.info(
    "Text embedding obtained: %s",
    JSON.stringify(textEmbeddingResponse.features, null, 2)
  );

  // Get Weaviate collection
  const resCollection = await getCollection();
  if (resCollection.error) {
    return {
      error: true,
      details: resCollection.details || "Error getting collection",
    };
  }

  const collection = resCollection.collection;
  const offset = page * limit;

  let result;

  // First try: nearVector with text_vector
  const queryOptions = {
    limit: limit,
    offset: offset,
    returnProperties: [
      "name",
      "description",
      "imageUrl",
      "url",
      "brandId",
      "price",
    ],
    nearVector: {
      text_vector: textEmbeddingResponse.features, // Use the named vector key
    },
    targetVector: ["text_vector"], // Specify which vector space to search
  };

  result = await collection.query.fetchObjects(queryOptions);

  logger.info(
    "Weaviate search completed, found %s objects",
    result.objects.length
  );

  // Map results to ItemSchemaType
  const items: ItemSchemaType[] = result.objects
    .map(match => {
      const item = {
        name: match.properties?.name || "",
        description: match.properties?.description || "",
        imageUrl: match.properties?.imageUrl || "",
        url: match.properties?.url || "",
        brandId: match.properties?.brandId || "",
        price: match.properties?.price || 0,
        uuid: match.uuid || "",
      };

      const validationResult = ItemSchema.safeParse(item);
      if (!validationResult.success) {
        logger.warn("Item validation failed:", validationResult.error);
        return null;
      }
      return validationResult.data;
    })
    .filter((item): item is ItemSchemaType => {
      console.log("item", item?.name);
      return item !== null;
    });

  logger.info(
    "Search completed successfully, returning %s items",
    items.length
  );

  return {
    error: false,
    items: items,
  };
}

